"""
Neural Observatory — HTML Reporter
"""
from __future__ import annotations

import datetime
import html
from pathlib import Path
from typing import List, Optional, Union

from .base import BaseReporter, Report
from ..analysis.base import AnalysisResult, AnalysisStatus
from ..core.configuration import ObservatoryConfig


_STATUS_COLORS = {
    AnalysisStatus.HEALTHY:  ("#d4edda", "#155724"),
    AnalysisStatus.INFO:     ("#d1ecf1", "#0c5460"),
    AnalysisStatus.WARNING:  ("#fff3cd", "#856404"),
    AnalysisStatus.CRITICAL: ("#f8d7da", "#721c24"),
}

# Inline CSS so the generated report is a single, portable standalone file.
