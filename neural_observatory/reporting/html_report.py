class HTMLReporter(BaseReporter):
    def __init__(self, output_path: Union[str, Path, None] = None) -> None:
        self._path = Path(output_path or "observatory_report.html")

    def report(
        self,
        report: Report,
        config: Optional[ObservatoryConfig] = None,
    ) -> None:
        cfg = config or ObservatoryConfig()
        html_str = self._render(report, cfg)
        self._path.write_text(html_str, encoding="utf-8")
        print(f"[HTMLReporter] Report written to {self._path.resolve()}")

    def _render(self, report: Report, cfg: ObservatoryConfig) -> str:
        results = report.filtered(
            min_severity=cfg.report_min_severity,
            max_results=cfg.report_max_layers,
            include_healthy=cfg.report_include_healthy,
        )

        summary = report.summary_dict()
        ts = datetime.datetime.fromtimestamp(report.timestamp).strftime(
            "%Y-%m-%d %H:%M:%S"
        )

        body_parts = [
            self._summary_card(summary, ts),
        ]

        by_analyzer: dict = {}
        for r in results:
            by_analyzer.setdefault(r.analyzer_name, []).append(r)

        for name, res in sorted(by_analyzer.items()):
            body_parts.append(self._section(name, res))

        body = "\n".join(body_parts)
        footer = f'<footer>Generated by Neural Observatory · {ts}</footer>'

        return f"""<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>Neural Observatory Report — Step {report.step}</title>
  <style>{_CSS}</style>
</head>
<body>
  <h1>🔭 Neural Observatory</h1>
  <p class="subtitle">
    Step {report.step} &nbsp;·&nbsp; Epoch {report.epoch} &nbsp;·&nbsp; {ts}
  </p>
  {body}
  {footer}
</body>
</html>"""

    def _summary_card(self, summary: dict, ts: str) -> str:
        status = summary["overall_status"]
        severity = summary["max_severity"]
        sc = summary["status_counts"]
        overall_status = AnalysisStatus(status)
        _, fg = _STATUS_COLORS.get(overall_status, ("#f8f9fa", "#333"))

        return f"""
<div class="summary-card">
  <div class="summary-item">
    <div class="value" style="color:{fg}">{status.upper()}</div>
    <div class="label">Overall Status</div>
  </div>
  <div class="summary-item">
    <div class="value" style="color:#dc3545">{sc.get('critical', 0)}</div>
    <div class="label">Critical</div>
  </div>
  <div class="summary-item">
    <div class="value" style="color:#ffc107">{sc.get('warning', 0)}</div>
    <div class="label">Warnings</div>
  </div>
  <div class="summary-item">
    <div class="value" style="color:#28a745">{sc.get('healthy', 0)}</div>
    <div class="label">Healthy</div>
  </div>
  <div class="summary-item">
    <div class="value">{severity:.2f}</div>
    <div class="label">Max Severity</div>
  </div>
</div>"""

    def _section(self, analyzer_name: str, results: List[AnalysisResult]) -> str:
        title = analyzer_name.upper().replace("_", " ")
        rows = "\n".join(self._row(r) for r in results)
        return f"""
<div class="section">
  <div class="section-header">{html.escape(title)}</div>
  <table>
    <thead>
      <tr>
        <th>Layer</th>
        <th>Status</th>
        <th>Severity</th>
        <th>Key Metrics</th>
        <th>Notes</th>
      </tr>
    </thead>
    <tbody>{rows}</tbody>
  </table>
</div>"""

    def _row(self, r: AnalysisResult) -> str:
        bg, fg = _STATUS_COLORS.get(r.status, ("#f8f9fa", "#333"))
        badge = (
            f'<span class="badge" style="background:{bg};color:{fg}">'
            f'{r.status.value.upper()}</span>'
        )

        metrics_html = self._metrics_html(r.metrics)

        warnings_html = ""
        if r.warnings:
            items = "".join(
                f'<div class="warning-item">⚠ {html.escape(w)}</div>'
                for w in r.warnings
            )
            warnings_html = f'<div class="warnings">{items}</div>'

        return f"""<tr>
  <td><code>{html.escape(r.layer_name)}</code></td>
  <td>{badge}</td>
  <td>{r.severity:.3f}</td>
  <td class="metric">{metrics_html}</td>
  <td>{warnings_html}</td>
</tr>"""

    @staticmethod
    def _metrics_html(metrics: dict) -> str:
        key_metrics = {
            k: v for k, v in metrics.items()
            if k not in ("num_observations", "has_nan", "has_inf",
                         "total_anomalies", "numel")
        }
        parts = []
        for k, v in list(key_metrics.items())[:3]:
            if isinstance(v, float):
                parts.append(f'<span class="metric-key">{html.escape(k)}=</span>'
                             f'<span>{v:.4f}</span>')
            else:
                parts.append(f'<span class="metric-key">{html.escape(k)}=</span>'
                             f'<span>{v}</span>')
        return " &nbsp; ".join(parts)